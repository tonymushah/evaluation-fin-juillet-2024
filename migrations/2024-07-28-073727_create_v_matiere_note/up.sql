create view v_matiere_note as
SELECT
    NOTE.ID_NOTE,
    NOTE.ETUDIANT,
    NOTE.MATIERE,
    NOTE.SUBMISSION,
    NOTE.VALEUR,
    MATIERE.NOM,
    MATIERE.SEMESTRE,
    MATIERE.CREDITS,
    MATIERE.OPTIONEL
FROM
    NOTE
    JOIN MATIERE ON MATIERE.ID_MATIERE = NOTE.MATIERE;