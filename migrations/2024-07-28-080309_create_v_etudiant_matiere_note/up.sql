create view v_etudiant_matiere_note as
SELECT
    V_EN.ETU,
    V_EN.ETU_NOM,
    V_EN.ETU_PRENOM,
    V_EN.ETU_DTN,
    V_EN.ETU_PROM,
    V_EN.ID_NOTE,
    V_EN.MATIERE,
    V_EN.SUBMISSION,
    V_EN.VALEUR,
    V_MN.NOM AS MATIERE_NOM,
    V_MN.SEMESTRE,
    V_MN.CREDITS AS MATIERE_CREDITS,
    V_MN.OPTIONEL AS MATIERE_OPTIONEL
FROM
    V_ETUDIANT_NOTE AS V_EN
    JOIN V_MATIERE_NOTE AS V_MN ON V_EN.ID_NOTE = V_MN.ID_NOTE