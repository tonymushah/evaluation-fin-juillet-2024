create view v_etudiant_note as
SELECT
    ETUDIANT.ETU,
    ETUDIANT.NOM AS ETU_NOM,
    ETUDIANT.PRENOM AS ETU_PRENOM,
    ETUDIANT.DATE_NAISSANCE AS ETU_DTN,
    ETUDIANT.PROMOTION AS ETU_PROM,
    NOTE.ID_NOTE,
    NOTE.MATIERE,
    NOTE.SUBMISSION,
    NOTE.VALEUR
FROM
    ETUDIANT
    JOIN NOTE ON ETUDIANT.ETU = NOTE.ETUDIANT;